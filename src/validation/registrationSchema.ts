/**
 * Registration Validation Schema - схемы валидации для регистрации
 */

import * as yup from 'yup';

/**
 * Схема для bondTime (предпочитаемое время связи)
 */
const bondTimeSchema = yup.object({
  bondTimeStart: yup
    .string()
    .matches(/^([01]\d|2[0-3]):([0-5]\d)$/, 'Неверный формат времени (ожидается HH:mm)')
    .required('Начало времени обязательно'),
  bondTimeEnd: yup
    .string()
    .matches(/^([01]\d|2[0-3]):([0-5]\d)$/, 'Неверный формат времени (ожидается HH:mm)')
    .required('Конец времени обязателен'),
});

/**
 * Схема для contactInfo (способы связи)
 */
const contactInfoSchema = yup.object({
  type: yup
    .string()
    .oneOf(['PHONE', 'EMAIL', 'TELEGRAM', 'VK'], 'Неверный тип контакта')
    .required('Тип контакта обязателен'),
  contact: yup
    .string()
    .required('Контакт обязателен для заполнения'),
  visible: yup
    .boolean()
    .required('Укажите видимость контакта'),
});

/**
 * Схема валидации для формы регистрации
 * Соответствует требованиям из API документации
 */
export const registrationSchema = yup.object({
  // Email: классический regexp под почты, максимум 64 символа
  email: yup
    .string()
    .email('Введите корректный email')
    .max(64, 'Email не должен превышать 64 символа')
    .required('Email обязателен для заполнения'),

  // ФИО: русские/латинские буквы, пробелы, дефисы
  firstName: yup
    .string()
    .required('Имя обязательно для заполнения')
    .matches(/^[a-zA-Zа-яА-ЯёЁ\s\-']+$/, 'Имя должно содержать только русские и латинские буквы'),

  secondName: yup
    .string()
    .required('Фамилия обязательна для заполнения')
    .matches(/^[a-zA-Zа-яА-ЯёЁ\s\-']+$/, 'Фамилия должна содержать только русские и латинские буквы'),

  lastName: yup
    .string()
    .required('Отчество обязательно для заполнения')
    .matches(/^[a-zA-Zа-яА-ЯёЁ\s\-']+$/, 'Отчество должно содержать только русские и латинские буквы'),

  // Пароль: от 8 символов, минимум одна буква верхнего и нижнего регистра, минимум одна цифра, максимум 64 символа
  password: yup
    .string()
    .required('Пароль обязателен для заполнения')
    .min(8, 'Пароль должен содержать минимум 8 символов')
    .max(64, 'Пароль не должен превышать 64 символа')
    .matches(/[A-Z]/, 'Пароль должен содержать хотя бы одну заглавную букву')
    .matches(/[a-z]/, 'Пароль должен содержать хотя бы одну строчную букву')
    .matches(/\d/, 'Пароль должен содержать хотя бы одну цифру'),

  // Подтверждение пароля: должен точно совпадать с полем пароль
  confirmPassword: yup
    .string()
    .required('Подтверждение пароля обязательно')
    .oneOf([yup.ref('password')], 'Пароли должны совпадать'),

  // Пол: M/F
  gender: yup
    .string()
    .oneOf(['M', 'F'], 'Выберите пол')
    .required('Пол обязателен для заполнения'),

  // Город проживания
  city: yup
    .string()
    .required('Город обязателен для заполнения'),

  region: yup
    .string()
    .required('Регион обязателен для заполнения'),

  // Предпочитаемое время связи (массив интервалов)
  bondTime: yup
    .array()
    .of(bondTimeSchema)
    .min(1, 'Укажите хотя бы один интервал времени для связи')
    .required('Время связи обязательно для заполнения'),

  // Способы связи (массив контактов)
  contactInfo: yup
    .array()
    .of(contactInfoSchema)
    .min(1, 'Укажите хотя бы один способ связи')
    .required('Способы связи обязательны для заполнения'),

  // Чекбокс согласия с условиями
  agreedToTerms: yup
    .boolean()
    .oneOf([true], 'Вы должны согласиться с условиями использования')
    .required('Вы должны согласиться с условиями использования'),
}).required();

export type RegistrationFormData = yup.InferType<typeof registrationSchema>;
